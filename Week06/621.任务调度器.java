class Solution {
    public int leastInterval(char[] tasks, int n) {
        int[] count = new int[26];
        for (int i = 0; i < tasks.length; i++) {
            count[tasks[i] - 'A']++;
        }
        Arrays.sort(count);
        int res = 0;
        while (count[25] > 0) {
            for (int i = 0; i < n + 1; i++) {
                if (i < 26 && count[25 - i] == 0 && count[25] == 0) {
                    break;
                }
                if (i < 26 && count[25 - i] > 0) {
                    count[25 - i]--;
                }
                res++;
            }
            Arrays.sort(count);
        }
        return res;
    }
}
